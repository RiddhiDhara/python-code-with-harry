# arithmatic operators
b = 8
c = 2

a = b + c
d = b - c
e = b * c
f = b / c
g = b // c

print(a , d, e , f , g)

# assignment operators
x = 1
y = 2
z = 3
v = 4

x += 1
y -= 1
z *= 3
v /= 2

print(x , y , z , v)

# comparison operators

print(b == c)
print(b >= c)
print(b <= c)
print(b != c)

# logical operators
k = True
j = False

print(k and j)
print(k or j)
print(not k)